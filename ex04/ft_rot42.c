char	*ft_rot42(char *str)
{
	int		i;
	char	j;

	i = 0;
	j = 'a';
	while (str[i])
	{
		if (str[i] >= 33 && str[i] <= 126)
		{
			j = ((((str[i] - 32) + 42) % 94) + 32);
			str[i] = j;
		}
		i++;
	}
	return (str);
}
